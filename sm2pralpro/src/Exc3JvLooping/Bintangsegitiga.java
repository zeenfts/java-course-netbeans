
package Exc3JvLooping;

public class Bintangsegitiga 
{
    public static void main(String[] args) 
    {
     for (int y=1; y<10; y++)
     {
      for (int z=0; z<y; z++)
      {
       System.out.print("*"+" ");          
      }
      System.out.println();
     }
     for (int y=9; y>0; y--)
     {
      for (int z=1; z<y; z++)
      {
       System.out.print("*"+" ");          
      }
      System.out.println();
     }
    
    }
    
}
